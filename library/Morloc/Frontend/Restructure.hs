-- | Resolve type aliases, term aliases and import/exports
restructure ::
  DAG MVar Import ExprI ->
  MorlocMonad (DAG MVar [AliasedSymbol] ExprI)
restructure s = do
  -- Set the counter for reindexing expressions.
  --
  -- This is the first use of the morloc state counter. The indices currently
  -- in the tree were generated by the lexer state counter. So now we need
  -- to find the largest index in the tree and resume counting from there.
  -- Since d is the entire tree, the initizalized counter will start at global maximum.
  MM.setCounter $ maximum (map AST.maxIndex (DAG.nodes s)) + 1

  checkForSelfRecursion s -- bare self-recursion is rejected; guarded forms pass
    >>= checkMutualRecursion -- non-trivial SCCs over typedef-references are rejected
    >>= resolveImports -- rewrite DAG edges to map imported terms to their aliases
    >>= handleBinops -- resolve binary operators
    >>= hoistEvals -- hoist user '!' markers into do-block binds
    >>= refineKinds -- promote VarU to NatVarU based on typedef param kinds (before self-defs are removed)
      |>> handleTypeDeclarations
    >>= doM collectTags
    >>= doM collectTypes
    >>= (\x -> collectUniversalTypes x >> return x)
    >>= doM collectSources

doM :: (Monad m) => (a -> m ()) -> a -> m a
doM f x = f x >> return x

-- Recursion-occurrence status for a typedef body. We need three
-- outcomes, not two: a guarded reference (legal) must dominate an
-- absent one, and a bare reference (illegal) must dominate everything.
data RecStatus = Absent | Guarded | Bare
  deriving (Eq, Show)

instance Semigroup RecStatus where
  Bare    <> _       = Bare
  _       <> Bare    = Bare
  Guarded <> _       = Guarded
  _       <> Guarded = Guarded
  _       <> _       = Absent

instance Monoid RecStatus where
  mempty = Absent

-- | Classify occurrences of @v@ inside a typedef body. The @guarded@
-- flag flips to True once we descend under an indirection that bounds
-- the size of the recursive payload at value-level:
--
--   * @OptionalU t@ (the @?_@ surface form) -- a recursive reference
--     inside is heap-indirected via the optional discriminant.
--   * @AppU (VarU (TV "List")) [t]@ -- a list field is empty-base-cased
--     and stored via a pointer to a heap region.
--
-- Once guarded, the flag stays True for all sub-terms. Any bare
-- occurrence (@VarU v@ with @guarded == False@) poisons the result
-- to @Bare@ and the whole typedef is rejected.
--
-- This runs BEFORE alias resolution, so we cannot peek through
-- user-level aliases like @type MyList a = List a@: such aliases are
-- treated as ordinary type applications and do NOT count as guards.
classifyRecursion :: TVar -> TypeU -> RecStatus
classifyRecursion v = go False
  where
    go :: Bool -> TypeU -> RecStatus
    go _ (OptionalU t)                       = go True t
    go _ (AppU (VarU (TV "List")) [t])       = go True t
    go g (VarU v') | v == v'                 = if g then Guarded else Bare
    go _ (VarU _)                            = Absent
    go g (ForallU _ t)                       = go g t
    go g (FunU ts r)                         = mconcat (go g r : map (go g) ts)
    go g (AppU h ts)                         = mconcat (go g h : map (go g) ts)
    go g (NamU _ _ ps rs)                    = mconcat (map (go g) ps ++ map (go g . snd) rs)
    go g (EffectU _ t)                       = go g t
    go g (OpU _ args)                        = mconcat (map (go g) args)
    go g (LitU (LRec fs))                    = mconcat (map (go g . snd) fs)
    go g (LitU (LList es))                   = mconcat (map (go g) es)
    go g (LitU (LSet es))                    = mconcat (map (go g) es)
    go g (LabeledU _ t)                      = go g t
    go _ ExistU {} = error "There should not be existentionals in typedefs"
    -- Inert: kind-specific vars/voids and Nat/Str literals.
    go _ _                                   = Absent

{- | Reject typedefs whose self-recursion would diverge.

Self-recursion through @[_]@ or @?_@ is legal: the recursive field is
heap-indirected and the base case (empty list, @null@) terminates
expansion. Any other self-reference -- bare under record field, tuple,
function arrow, or non-list application -- is rejected because such a
typedef would have unbounded structural size.

Examples:

  type (Tree n) = {value :: n, children :: [Tree n]}  -- legal (list-guarded)
  type LL = {head :: Int, tail :: ?LL}                 -- legal (option-guarded)
  type Bad = {a :: Bad}                                -- rejected (bare)

The exemption at the head-application case allows a typedef body to
mention the type applied to its own parameters, which is the parser's
shape for parametric existence claims like @type Foo a = Foo a@.

Mutual recursion is NOT diagnosed here; @checkMutualRecursion@ runs
afterwards and rejects any non-trivial SCC over typedef references.
-}
checkForSelfRecursion :: DAG k e ExprI -> MorlocMonad (DAG k e ExprI)
checkForSelfRecursion d = do
  _ <- DAG.mapNodeM (AST.checkExprI isExprSelfRecursive) d
  return d
  where
    isExprSelfRecursive :: ExprI -> MorlocMonad ()
    isExprSelfRecursive (ExprI i (TypE (ExprTypeE Nothing v vs t _ _)))
      -- Forward declaration with no body: parser lowers @type Foo@ to a
      -- body that is just @VarU Foo@ itself. Exempt these along with the
      -- parametric self-application case @type Foo a = Foo a@.
      | t == VarU v = return ()
      | t == AppU (VarU v) (map (either (VarU . fst) id) vs) = return ()
      | classifyRecursion v t == Bare =
          MM.throwSourcedError i $ "Found unsupported self-recursive type alias:" <+> pretty v
      | otherwise = return ()
    isExprSelfRecursive (ExprI i (TypE (ExprTypeE _ v ts t _ _)))
      -- Language-specific typedefs: the same rule applied to the body
      -- and any TypeU parameter slots.
      | any ((== Bare) . classifyRecursion v) (t : rights ts) =
          MM.throwSourcedError i $ "Found unsupported self-recursive type alias:" <+> pretty v
      | otherwise = return ()
    isExprSelfRecursive _ = return ()

{- | Reject mutually recursive typedefs.

Self-recursion guarded by @[_]@ or @?_@ is permitted by
@checkForSelfRecursion@. Mutual recursion -- a cycle of two or more
typedefs that reference each other -- is not supported, because the
type-evaluator's bound-set termination only protects each typedef's
own name; cycles across distinct typedef names would still loop. We
diagnose them up front here so the compiler does not hang later in
the reduce-and-retry sites in Realize/Express or in pairEval's alias
chase.

Algorithm: collect typedefs across ALL modules in the DAG and build a
single directed graph keyed by @(Maybe Lang, TVar)@ -- the typedef's
language scope (general = @Nothing@, language-specific = @Just lang@)
paired with its name. An edge @A -> B@ exists iff @B@ appears
anywhere in @A@'s body (regardless of guarding); references resolve
to the same-language entry when one exists, otherwise to the general
entry. Quotient by SCC via @Data.Graph.stronglyConnComp@. Any cyclic
SCC of size >= 2 is illegal mutual recursion. Singleton SCCs (with
or without a self-loop) are passed through: a self-loop indicates a
self-recursion that @checkForSelfRecursion@ has already either
permitted (guarded) or rejected (bare).

The check is global rather than per-module because cycles can span
module boundaries: a module's typedef can reference an imported name
whose source module's typedef references back into the importer.
Each module's view looks acyclic in isolation; only the merged
typedef graph reveals the cycle.

Per-language keying matters because a language-specific cycle
(e.g. @type Cpp => A = "..." B@ + @type Cpp => B = "..." A@) is
invisible to a check that only collects general typedefs, even
though it would hang @pairEval@ when codegen resolves a concrete
type. It also avoids false positives when two languages happen to
share typedef names without interacting.

Bound parameter names of a typedef are excluded from the outgoing
references, so @type Foo a = Foo a@ does not spuriously self-edge
through the @a@ parameter.
-}
checkMutualRecursion :: DAG k e ExprI -> MorlocMonad (DAG k e ExprI)
checkMutualRecursion d = do
  let allTypedefs = concatMap collectTypedefs (DAG.nodes d)
      declared = Set.fromList [(scope, v) | (_, scope, v, _, _) <- allTypedefs]
      -- Resolve a bare name reference to the right scope-qualified
      -- key. Same-language scope wins; otherwise fall back to the
      -- general scope. References to undeclared names (built-ins,
      -- concrete-name strings) produce no edge.
      resolveRef scope x
        | Set.member (scope, x) declared = Just (scope, x)
        | Set.member (Nothing, x) declared = Just (Nothing, x)
        | otherwise = Nothing
      nodes =
        [ ((i, scope, v), (scope, v), Set.toList outgoing)
        | (i, scope, v, vs, t) <- allTypedefs
        , let bound = Set.fromList [p | Left (p, _) <- vs]
        , let mentioned = Set.fromList (AST.findTypeTerms t) `Set.difference` bound
        , let outgoing = Set.fromList (mapMaybe (resolveRef scope) (Set.toList mentioned))
        ]
      sccs = Graph.stronglyConnComp nodes
  mapM_ rejectCyclic sccs
  return d
  where
    rejectCyclic :: Graph.SCC (Int, Maybe Lang, TVar) -> MorlocMonad ()
    rejectCyclic (Graph.CyclicSCC xs@(_:_))
      | length xs >= 2 =
          let (firstIdx, scope0, _) = head xs
              cycleNames = [v | (_, _, v) <- xs]
              -- An SCC of size >= 2 has all members in the same
              -- scope: general typedefs never reference
              -- language-specific names, and language-to-general
              -- edges are one-way (the general body cannot close
              -- the cycle back to a language scope).
              scopeMsg = case scope0 of
                Nothing -> "type definitions"
                Just lang -> pretty lang <+> "type definitions"
          in MM.throwSourcedError firstIdx $
               "Mutual recursion between" <+> scopeMsg <+> "is not supported." <+>
               "Cycle:" <+> hsep (punctuate "," (map pretty cycleNames))
    rejectCyclic _ = return ()

    collectTypedefs :: ExprI -> [(Int, Maybe Lang, TVar, [Either (TVar, Kind) TypeU], TypeU)]
    collectTypedefs (ExprI i (TypE (ExprTypeE form v vs t _ _))) = [(i, fmap fst form, v, vs, t)]
    collectTypedefs (ExprI _ (ModE _ es)) = concatMap collectTypedefs es
    collectTypedefs _ = []

{- | Use export/import information to find which terms are imported into each module
* reduces the Import edge type to an alias map.
* replaces Export terms in expressions
-}
resolveImports ::
  DAG MVar Import ExprI ->
  MorlocMonad (DAG MVar [AliasedSymbol] ExprI)
resolveImports d0 =
  DAG.synthesize resolveExports resolveEdge d0
    >>= maybe (MM.throwSystemError "Cyclical import dependency in resolveImports") return
  where
    -- Map every typeclass method name in the DAG to the class that owns it.
    -- Used to produce a targeted diagnostic when a user attempts to selectively
    -- import a method; method names are never exported as standalone symbols.
    classMethods :: Map EVar ClassName
    classMethods = foldr (Map.union . findClassMethodsInExpr) Map.empty (DAG.nodes d0)

    findClassMethodsInExpr :: ExprI -> Map EVar ClassName
    findClassMethodsInExpr (ExprI _ (ModE _ es)) = Map.unions (map findClassMethodsInExpr es)
    findClassMethodsInExpr (ExprI _ (ClsE (Typeclass _ cls _ sigs))) =
      Map.fromList [(name, cls) | Signature name _ _ <- sigs]
    findClassMethodsInExpr _ = Map.empty

    -- Collect all exported terms from a module (including those imported
    -- without qualification. Then update the ExpE term
    resolveExports :: MVar -> ExprI -> [(MVar, Import, ExprI)] -> MorlocMonad ExprI
    resolveExports m e children = do
      let allLocalSymbols = findSymbols e -- Set Symbol
          export = AST.findExport e -- Export
      allImportedSymbols <-
        Set.unions <$> mapM (\(_, imp', expr') -> filterImports m imp' (AST.findExport expr')) children

      let allSymbols = Set.union allLocalSymbols allImportedSymbols

      case export of
        ExportAll -> do
          exports <- mapM addIndex (Set.toList allSymbols) |>> Set.fromList
          return $ AST.setExport (ExportMany exports []) e
        (ExportMany ungroupedExports groups) ->
          let allExplicit = Set.unions (ungroupedExports : [exportGroupMembers g | g <- groups])
              resolved = resolveExplicitTypeclasses allSymbols allExplicit
              missing = Set.map snd resolved `Set.difference` allSymbols
           in if Set.null missing
                then do
                  -- Rebuild groups with resolved typeclasses
                  let resolvedGroups =
                        map
                          (\g -> g {exportGroupMembers = resolveExplicitTypeclasses allSymbols (exportGroupMembers g)})
                          groups
                      resolvedUngrouped = resolveExplicitTypeclasses allSymbols ungroupedExports
                  return $ AST.setExport (ExportMany resolvedUngrouped resolvedGroups) e
                else
                  MM.throwSystemError $
                    "Module"
                      <+> squotes (pretty m)
                      <+> "does not export the following terms or types:"
                      <+> list (map pretty (Set.toList missing))

    resolveExplicitTypeclasses :: Set Symbol -> Set (Int, Symbol) -> Set (Int, Symbol)
    resolveExplicitTypeclasses ss sis = Set.map f sis
      where
        f :: (Int, Symbol) -> (Int, Symbol)
        f (i, TypeSymbol (TV x))
          | (ClassSymbol (ClassName x)) `Set.member` ss = (i, ClassSymbol (ClassName x))
          | otherwise = (i, TypeSymbol (TV x))
        f x = x

    addIndex :: a -> MorlocMonad (Int, a)
    addIndex x = (,) <$> MM.getCounter <*> pure x

    -- TODO: distinguish between these expressions at the type-level
    --       the contains unresolved imports, that later resolved
    resolveEdge ::
      Import ->
      ExprI -> -- importing module expression (with resolved exports)
      ExprI -> -- imported module expression  (with resolved exports)
      MorlocMonad [AliasedSymbol]
    resolveEdge imp _ childX = case (importInclude imp, importNamespace imp, AST.findExport childX) of
      (_, _, ExportAll) -> error "This should have been resolved already"
      -- No namespace: existing behavior
      (Nothing, Nothing, ExportMany exps gs) ->
        let allExps = Set.unions (exps : [exportGroupMembers g | g <- gs])
         in return $ map (toAliasedSymbol . snd) (Set.toList allExps)
      (Just ass, Nothing, ExportMany exps gs) -> return . catMaybes $ map (importAlias . unAliasedSymbol) ass
        where
          allExps = Set.unions (exps : [exportGroupMembers g | g <- gs])
          exportMap = Map.fromList [(unSymbol s, s) | (_, s) <- Set.toList allExps]
          excludes = map unSymbol (importExclude imp)

          importAlias :: (Text, Text) -> Maybe AliasedSymbol
          importAlias (name, alias)
            | name `elem` excludes = Nothing
            | otherwise = case Map.lookup name exportMap of
                Nothing -> Nothing
                (Just (TermSymbol _)) -> Just $ AliasedTerm (EV name) (EV alias)
                (Just (TypeSymbol _)) -> Just $ AliasedType (TV name) (TV alias)
                (Just (ClassSymbol _)) -> Just $ AliasedClass (ClassName name)
      -- With namespace: prefix term aliases (V1: types/classes not prefixed)
      (Nothing, Just ns, ExportMany exps gs) ->
        let allExps = Set.unions (exps : [exportGroupMembers g | g <- gs])
         in return $ map (prefixAlias ns . toAliasedSymbol . snd) (Set.toList allExps)
      (Just ass, Just ns, ExportMany exps gs) -> return . catMaybes $ map (importAlias . unAliasedSymbol) ass
        where
          allExps = Set.unions (exps : [exportGroupMembers g | g <- gs])
          exportMap = Map.fromList [(unSymbol s, s) | (_, s) <- Set.toList allExps]
          excludes = map unSymbol (importExclude imp)

          importAlias :: (Text, Text) -> Maybe AliasedSymbol
          importAlias (name, alias)
            | name `elem` excludes = Nothing
            | otherwise = case Map.lookup name exportMap of
                Nothing -> Nothing
                (Just (TermSymbol _)) -> Just $ prefixAlias ns (AliasedTerm (EV name) (EV alias))
                (Just (TypeSymbol _)) -> Just $ AliasedType (TV name) (TV alias)
                (Just (ClassSymbol _)) -> Just $ AliasedClass (ClassName name)

    prefixAlias :: EVar -> AliasedSymbol -> AliasedSymbol
    prefixAlias (EV ns) (AliasedTerm orig (EV alias)) = AliasedTerm orig (EV (ns <> "." <> alias))
    prefixAlias _ sym = sym

    filterImports ::
      MVar ->
      Import -> -- the current node import list
      Export -> -- the imported modules export list
      MorlocMonad (Set Symbol)
    -- No namespace, no include list: import everything
    filterImports _ (Import _ Nothing exclude Nothing) (ExportMany exports gs) =
      let allExports = Set.unions (exports : [exportGroupMembers g | g <- gs])
       in return $ (Set.map snd allExports) `Set.difference` (Set.fromList exclude)
    -- With namespace, no include list: prefix all terms
    filterImports _ (Import _ Nothing exclude (Just (EV ns))) (ExportMany exports gs) =
      let allExports = Set.unions (exports : [exportGroupMembers g | g <- gs])
       in return $ Set.map (prefixSymbol ns) ((Set.map snd allExports) `Set.difference` (Set.fromList exclude))
    -- No namespace, with include list: existing behavior
    filterImports m1 (Import m2 (Just as) (map unSymbol -> exclude) Nothing) (ExportMany exports gs) =
      case partitionEithers . catMaybes $ map importAlias (map unAliasedSymbol as) of
        ([], imps) -> return $ Set.fromList imps
        (missing, _) -> throwMissingImportError m1 m2 missing
      where
        allExports = Set.unions (exports : [exportGroupMembers g | g <- gs])
        exportMap = Map.fromList [(unSymbol s, s) | (_, s) <- Set.toList allExports]

        importAlias :: (Text, Text) -> Maybe (Either Text Symbol)
        importAlias (name, alias)
          | name `elem` exclude = Nothing
          | otherwise = case Map.lookup name exportMap of
              Nothing -> Just (Left name)
              (Just (TermSymbol _)) -> Just . Right $ TermSymbol (EV alias)
              (Just (TypeSymbol _)) -> Just . Right $ TypeSymbol (TV alias)
              (Just (ClassSymbol _)) -> Just . Right $ ClassSymbol (ClassName alias)
    -- With namespace and include list: prefix selected terms
    filterImports m1 (Import m2 (Just as) (map unSymbol -> exclude) (Just (EV ns))) (ExportMany exports gs) =
      case partitionEithers . catMaybes $ map importAlias (map unAliasedSymbol as) of
        ([], imps) -> return $ Set.fromList imps
        (missing, _) -> throwMissingImportError m1 m2 missing
      where
        allExports = Set.unions (exports : [exportGroupMembers g | g <- gs])
        exportMap = Map.fromList [(unSymbol s, s) | (_, s) <- Set.toList allExports]

        importAlias :: (Text, Text) -> Maybe (Either Text Symbol)
        importAlias (name, alias)
          | name `elem` exclude = Nothing
          | otherwise = case Map.lookup name exportMap of
              Nothing -> Just (Left name)
              (Just (TermSymbol _)) -> Just . Right $ TermSymbol (EV (ns <> "." <> alias))
              (Just (TypeSymbol _)) -> Just . Right $ TypeSymbol (TV alias)
              (Just (ClassSymbol _)) -> Just . Right $ ClassSymbol (ClassName alias)
    filterImports _ _ _ = error "Unreachable -- all Export values should have been converted to ExportMany"

    -- Build the diagnostic for a failed selective import. Names that resolve
    -- to a typeclass method get a dedicated message explaining that methods
    -- are not nameable in import lists; truly absent names get the generic
    -- "not exported" message. If both are present, both sections appear.
    throwMissingImportError :: MVar -> MVar -> [Text] -> MorlocMonad a
    throwMissingImportError m1 m2 missing =
      let (methods, notExported) = partitionMissing missing
       in MM.throwSystemError $ vsep (methodSection methods m1 m2 ++ notExportedSection notExported m1 m2)

    methodSection :: [(Text, ClassName)] -> MVar -> MVar -> [MDoc]
    methodSection [] _ _ = []
    methodSection methods m1 m2 =
      [ "The following terms imported from"
          <+> squotes (pretty m1)
          <+> "are typeclass methods reachable from"
          <+> squotes (pretty m2)
          <> ", which cannot be imported by name:\n"
          <+> indent 2 (vsep [pretty name <+> "(method of class" <+> squotes (pretty cls) <> ")" | (name, cls) <- methods])
          <> "\n  Use an empty import list to bring instances into scope implicitly:"
          <+> squotes ("import" <+> pretty m2 <+> "()")
          <> "."
      ]

    notExportedSection :: [Text] -> MVar -> MVar -> [MDoc]
    notExportedSection [] _ _ = []
    notExportedSection names m1 m2 =
      [ "The terms imported from"
          <+> squotes (pretty m1)
          <+> "are not exported from module"
          <+> squotes (pretty m2)
          <> ":\n"
          <+> indent 2 (vsep (map pretty names))
      ]

    partitionMissing :: [Text] -> ([(Text, ClassName)], [Text])
    partitionMissing = foldr step ([], [])
      where
        step name (methods, notExported) =
          case Map.lookup (EV name) classMethods of
            Just cls -> ((name, cls) : methods, notExported)
            Nothing -> (methods, name : notExported)

    prefixSymbol :: Text -> Symbol -> Symbol
    prefixSymbol ns (TermSymbol (EV name)) = TermSymbol (EV (ns <> "." <> name))
    prefixSymbol _ sym = sym

    findSymbols :: ExprI -> Set Symbol
    findSymbols (ExprI _ (ModE _ es)) = Set.unions (map findSymbols es)
    findSymbols (ExprI _ (TypE (ExprTypeE _ v _ _ _ _))) = Set.singleton $ TypeSymbol v
    findSymbols (ExprI _ (AssE e _ _)) = Set.singleton $ TermSymbol e
    findSymbols (ExprI _ (ClsE (Typeclass _ cls _ _))) = Set.singleton $ ClassSymbol cls
    findSymbols (ExprI _ (SigE (Signature e _ _))) = Set.singleton $ TermSymbol e
    findSymbols (ExprI _ (SrcE src)) = Set.singleton $ TermSymbol (srcAlias src)
    -- The definition of an instance does not automatically imply export or make
    -- the values available. The instance is ALWAYS relative to the class
    -- definition (either local or imported).
    findSymbols (ExprI _ (IstE cls _ _)) = Set.singleton $ ClassSymbol cls
    findSymbols _ = Set.empty

    unSymbol :: Symbol -> Text
    unSymbol (TypeSymbol (TV v)) = v
    unSymbol (TermSymbol (EV v)) = v
    unSymbol (ClassSymbol (ClassName v)) = v

    unAliasedSymbol :: AliasedSymbol -> (Text, Text)
    unAliasedSymbol (AliasedType x y) = (unTVar x, unTVar y)
    unAliasedSymbol (AliasedTerm x y) = (unEVar x, unEVar y)
    unAliasedSymbol (AliasedClass x) = (unClassName x, unClassName x)

    toAliasedSymbol :: Symbol -> AliasedSymbol
    toAliasedSymbol (TypeSymbol x) = AliasedType x x
    toAliasedSymbol (TermSymbol x) = AliasedTerm x x
    toAliasedSymbol (ClassSymbol x) = AliasedClass x

handleTypeDeclarations ::
  DAG k e ExprI ->
  DAG k e ExprI
handleTypeDeclarations = DAG.mapNode f
  where
    f (ExprI i (ModE m es)) = ExprI i (ModE m (filter isNotSelfDef es))
    f e = e

    isNotSelfDef :: ExprI -> Bool
    isNotSelfDef (ExprI _ (TypE (ExprTypeE Nothing v [] (VarU v') _ _))) = v /= v'
    isNotSelfDef (ExprI _ (TypE (ExprTypeE Nothing (VarU -> v) (map (either (VarU . fst) id) -> vs) (AppU v' vs') _ _))) =
      v /= v' || length vs /= length vs' || not (all (uncurry (==)) (zip vs vs'))
    isNotSelfDef _ = True

handleBinops :: DAG MVar [AliasedSymbol] ExprI -> MorlocMonad (DAG MVar [AliasedSymbol] ExprI)
handleBinops d0 = do
  mayN <- DAG.synthesize updateNode (\e _ _ -> return e) d0
  case mayN of
    (Just e') -> return $ DAG.mapNode (\(e, _, _) -> e) e'
    Nothing -> error "Unreachable?"
  where
    updateNode ::
      MVar ->
      ExprI ->
      [(MVar, [AliasedSymbol], (ExprI, Map ClassName [EVar], Map EVar (Associativity, Int)))] ->
      MorlocMonad (ExprI, Map ClassName [EVar], Map EVar (Associativity, Int))
    updateNode _ e es = do
      thisFixityMap <- AST.findFixityMap e

      let clsOps = Map.unions $ findClassOps e : [filterClsOps ss cs | (_, ss, (_, cs, _)) <- es]
          clsOpSet = Set.fromList . concat $ Map.elems clsOps

      fixityMap <-
        mergeFixityMaps $ thisFixityMap : [filterTerms clsOpSet m ss | (_, ss, (_, _, m)) <- es]
      e' <- updateBinopExprs fixityMap e
      return (e', clsOps, fixityMap)

    filterClsOps :: [AliasedSymbol] -> Map ClassName [EVar] -> Map ClassName [EVar]
    filterClsOps ass clsmap =
      let clss = [cls | (AliasedClass cls) <- ass]
       in Map.filterWithKey (\k _ -> elem k clss) clsmap

    findClassOps :: ExprI -> Map ClassName [EVar]
    findClassOps (ExprI _ (ModE _ es)) = Map.unions (map findClassOps es)
    findClassOps (ExprI _ (ClsE (Typeclass _ cls _ sigs))) = Map.singleton cls [v | (Signature v _ _) <- sigs]
    findClassOps _ = Map.empty

    filterTerms :: Set EVar -> Map EVar a -> [AliasedSymbol] -> Map EVar a
    filterTerms cs m ss = Map.union unaliasedOps clsOps
      where
        symMap = Map.fromList [(k, v) | (AliasedTerm k v) <- ss]

        -- gather non-typeclass operator aliases
        unaliasedOps = Map.fromList . catMaybes $ [Map.lookup k symMap |>> (,v) | (k, v) <- Map.toList m]

        -- gather typeclass operators aliases
        clsOps = Map.filterWithKey (\k _ -> Set.member k cs) m

    mergeFixityMaps :: (Eq a) => [Map EVar a] -> MorlocMonad (Map EVar a)
    mergeFixityMaps [] = return Map.empty
    mergeFixityMaps [e1] = return e1
    mergeFixityMaps (e1 : e2 : es) = do
      e' <- foldlM strictInsert e1 (Map.toList e2)
      mergeFixityMaps (e' : es)

    strictInsert :: (Eq v) => Map EVar v -> (EVar, v) -> MorlocMonad (Map EVar v)
    strictInsert m (k, v) = case Map.lookup k m of
      Nothing -> return $ Map.insert k v m
      (Just v') ->
        if v == v'
          then return m
          else MM.throwSystemError $ "Conflicting fixity definitions for" <+> pretty k

    updateBinopExprs :: Map EVar (Associativity, Int) -> ExprI -> MorlocMonad ExprI
    updateBinopExprs m0 = f
      where
        f e@(ExprI _ BopE {}) = resolveBinop m0 e >>= f
        f (ExprI i (ModE m es)) = ModE m <$> mapM f es |>> ExprI i
        f (ExprI i (IstE cls ts es)) = IstE cls ts <$> mapM f es |>> ExprI i
        f (ExprI i (AssE v e es)) = AssE v <$> f e <*> mapM f es |>> ExprI i
        f (ExprI i (LstE es)) = LstE <$> mapM f es |>> ExprI i
        f (ExprI i (TupE es)) = TupE <$> mapM f es |>> ExprI i
        f (ExprI i (NamE rs)) = do
          es' <- mapM (f . snd) rs
          return $ ExprI i (NamE (zip (map fst rs) es'))
        f (ExprI i (AppE e es)) = AppE <$> f e <*> mapM f es |>> ExprI i
        f (ExprI i (LamE vs e)) = LamE vs <$> f e |>> ExprI i
        f (ExprI i (AnnE e t)) = AnnE <$> f e <*> pure t |>> ExprI i
        f (ExprI i (LetE bindings body)) = do
          bindings' <- mapM (\(v, e) -> (,) v <$> f e) bindings
          body' <- f body
          return $ ExprI i (LetE bindings' body')
        f (ExprI i (IfE c t e)) = IfE <$> f c <*> f t <*> f e |>> ExprI i
        f (ExprI i (DoBlockE e)) = DoBlockE <$> f e |>> ExprI i
        f (ExprI i (EvalE e)) = EvalE <$> f e |>> ExprI i
        f (ExprI i (IntrinsicE intr es)) = IntrinsicE intr <$> mapM f es |>> ExprI i
        f (ExprI _ (ParenE inner)) = f inner
        f e = return e

    -- \| Rewrite a right-nested BopE chain into a correctly-associated AppE tree.
    -- Uses the Pratt (precedence climbing) algorithm.
    -- Operators not in fixMap default to infixl 9.
    resolveBinop :: Map EVar (Associativity, Int) -> ExprI -> MorlocMonad ExprI
    resolveBinop fixMap expr = do
      let (lhs0, ops) = flatten expr
      (result, _) <- pratt 0 lhs0 ops
      return result
      where
        lookupFixity :: EVar -> (Associativity, Int)
        lookupFixity v = Map.findWithDefault (InfixL, 9) v fixMap

        -- Walk the right spine of BopE nodes into a flat list.
        -- Each entry: (outerIdx, opIdx, opName, rightOperand)
        flatten :: ExprI -> (ExprI, [(Int, Int, EVar, ExprI)])
        flatten (ExprI outerI (BopE lhs opI op rhs)) =
          let (rhsFirst, rhsRest) = flatten rhs
           in (lhs, (outerI, opI, op, rhsFirst) : rhsRest)
        flatten e = (e, [])

        -- Pratt loop: consume operators with prec >= minPrec.
        -- Returns the parsed lhs and the unconsumed tail.
        pratt :: Int -> ExprI -> [(Int, Int, EVar, ExprI)] -> MorlocMonad (ExprI, [(Int, Int, EVar, ExprI)])
        pratt _ lhs [] = return (lhs, [])
        pratt minPrec lhs ((outerI, opI, op, rhs) : rest) = do
          let (assoc, prec) = lookupFixity op
          if prec < minPrec
            then return (lhs, (outerI, opI, op, rhs) : rest)
            else do
              let nextMinPrec = if assoc == InfixR then prec else prec + 1
              (rhsParsed, remaining) <- pratt nextMinPrec rhs rest
              -- Ambiguity check: incompatible fixities at the same precedence
              case remaining of
                ((_, _, nextOp, _) : _) -> do
                  let (nextAssoc, nextPrec) = lookupFixity nextOp
                  when (nextPrec == prec && (assoc /= nextAssoc || assoc == InfixN)) . MM.throwSourcedError opI $
                    "Ambiguous use of" <+> pretty op <+> "and" <+> pretty nextOp
                      <> ": parenthesize or declare compatible fixities"
                [] -> return ()
              let lhs' = ExprI outerI $ AppE (ExprI opI (VarE defaultValue op)) [lhs, rhsParsed]
              pratt minPrec lhs' remaining

-- | Hoist user-written '!' (EvalE) markers into fresh do-block binds.
--
-- Runs after handleBinops, so BopE has been reassociated into AppE and does
-- not appear here. Walks every AssE RHS (including where-clause AssEs
-- recursively). Each EvalE at a non-bind position becomes a fresh '_eval_N'
-- bind in a wrapping DoBlockE; EvalE inside an existing do-block LetE-chain
-- becomes a bind inserted immediately before the containing statement,
-- preserving left-to-right effect order.
--
-- Output is the same LetE + EvalE + DoBlockE shape that desugarDo produces,
-- so typecheck and codegen require no changes.
hoistEvals :: DAG MVar [AliasedSymbol] ExprI -> MorlocMonad (DAG MVar [AliasedSymbol] ExprI)
hoistEvals = DAG.mapNodeM hoistNode
  where
    hoistNode :: ExprI -> MorlocMonad ExprI
    hoistNode (ExprI i (ModE m es)) = ExprI i . ModE m <$> mapM hoistNode es
    hoistNode (ExprI i (IstE cls ts es)) = ExprI i . IstE cls ts <$> mapM hoistNode es
    hoistNode (ExprI i (AssE v rhs whereDecls)) = do
      rhs' <- hoistBoundary rhs
      whereDecls' <- mapM hoistNode whereDecls
      return (ExprI i (AssE v rhs' whereDecls'))
    hoistNode e = return e

    freshEvalVar :: MorlocMonad EVar
    freshEvalVar = do
      i <- MM.getCounter
      return (EV ("_eval_" <> T.pack (show i)))

    wrapBoundary :: Int -> ExprI -> [(EVar, ExprI)] -> MorlocMonad ExprI
    wrapBoundary _ core [] = return core
    wrapBoundary parent core bs = do
      inner <- foldrM (nestLet parent) core bs
      blockI <- newIndex parent
      return (ExprI blockI (DoBlockE inner))

    nestLet :: Int -> (EVar, ExprI) -> ExprI -> MorlocMonad ExprI
    nestLet parent bind acc = do
      letI <- newIndex parent
      return (ExprI letI (LetE [bind] acc))

    hoistBoundary :: ExprI -> MorlocMonad ExprI
    hoistBoundary e@(ExprI parent _) = do
      (e', bs) <- hoistIn e
      wrapBoundary parent e' bs

    mapAndCollect ::
      (a -> MorlocMonad (b, [(EVar, ExprI)])) ->
      [a] ->
      MorlocMonad ([b], [(EVar, ExprI)])
    mapAndCollect f xs = do
      results <- mapM f xs
      let (ys, bss) = unzip results
      return (ys, concat bss)

    hoistIn :: ExprI -> MorlocMonad (ExprI, [(EVar, ExprI)])
    -- Inner hoists bind first so this bang's forced RHS sees them in scope.
    hoistIn (ExprI i (EvalE inner)) = do
      (inner', bs) <- hoistIn inner
      v <- freshEvalVar
      refI <- newIndex i
      return
        ( ExprI refI (VarE defaultValue v)
        , bs ++ [(v, ExprI i (EvalE inner'))]
        )
    hoistIn (ExprI i (DoBlockE inner)) = do
      inner' <- hoistDoChain inner
      return (ExprI i (DoBlockE inner'), [])
    hoistIn (ExprI i (LamE vs body)) = do
      body' <- hoistBoundary body
      return (ExprI i (LamE vs body'), [])
    hoistIn (ExprI i (IfE c t e)) = do
      (c', bs) <- hoistIn c
      t' <- hoistBoundary t
      e' <- hoistBoundary e
      return (ExprI i (IfE c' t' e'), bs)
    hoistIn (ExprI i (AnnE subject ty)) = do
      subject' <- hoistBoundary subject
      return (ExprI i (AnnE subject' ty), [])
    -- LetE is a self-boundary. Desugar rejects any hoistable bang in a
    -- binding RHS (see 'rejectHoistableBangInLetRhs'), so hoists from
    -- binding RHSs are always empty here -- we still recurse through
    -- them so a bang inside a nested boundary (lambda body, nested do,
    -- guard branch, ann subject, nested let) gets rewritten. Body
    -- hoists become fresh bindings appended after all original ones,
    -- and the whole let wraps in a DoBlockE so 'collectDoEffects'
    -- sees the effect firing point.
    hoistIn (ExprI i (LetE binds body)) = do
      binds' <- mapM (\(v, e) -> do (e', _) <- hoistIn e; return (v, e')) binds
      (body', bodyHoists) <- hoistIn body
      let letE = ExprI i (LetE (binds' ++ bodyHoists) body')
      if null bodyHoists
        then return (letE, [])
        else do
          blockI <- newIndex i
          return (ExprI blockI (DoBlockE letE), [])
    hoistIn (ExprI i (AppE f args)) = do
      (f', bs_f) <- hoistIn f
      (args', bs_args) <- mapAndCollect hoistIn args
      return (ExprI i (AppE f' args'), bs_f ++ bs_args)
    hoistIn (ExprI i (TupE es)) = do
      (es', bs) <- mapAndCollect hoistIn es
      return (ExprI i (TupE es'), bs)
    hoistIn (ExprI i (LstE es)) = do
      (es', bs) <- mapAndCollect hoistIn es
      return (ExprI i (LstE es'), bs)
    hoistIn (ExprI i (NamE rs)) = do
      (rs', bs) <- mapAndCollect
        (\(k, e) -> do (e', bs) <- hoistIn e; return ((k, e'), bs))
        rs
      return (ExprI i (NamE rs'), bs)
    -- @catch's args are boundaries: '!' inside a fallible or fallback
    -- seals at the arg site instead of hoisting above the @catch. Without
    -- this, `@catch !(foo x) fb` would rewrite to `do { v <- foo x; @catch
    -- v fb }`, evaluating the fallible outside @catch's try/catch scope
    -- and defeating the catch entirely (and rejecting at typecheck since
    -- the fallible would then be pure). Both args are sealed because the
    -- fallback is lazy at runtime -- a bang in fallback that hoisted up
    -- would fire eagerly even on the happy path.
    hoistIn (ExprI i (IntrinsicE IntrCatch [fallible, fallback])) = do
      fallible' <- hoistBoundary fallible
      fallback' <- hoistBoundary fallback
      return (ExprI i (IntrinsicE IntrCatch [fallible', fallback']), [])
    hoistIn (ExprI i (IntrinsicE intr es)) = do
      (es', bs) <- mapAndCollect hoistIn es
      return (ExprI i (IntrinsicE intr es'), bs)
    hoistIn (ExprI i (ParenE inner)) = do
      (inner', bs) <- hoistIn inner
      return (ExprI i (ParenE inner'), bs)
    -- BopE should be eliminated by handleBinops before this pass; handle
    -- defensively so any surviving BopE still gets its EvalEs hoisted.
    hoistIn (ExprI i (BopE lhs opI op rhs)) = do
      (lhs', bs_l) <- hoistIn lhs
      (rhs', bs_r) <- hoistIn rhs
      return (ExprI i (BopE lhs' opI op rhs'), bs_l ++ bs_r)
    hoistIn e = return (e, [])

    -- Walks a LetE-chain produced by desugarDo. Bangs in each binding's
    -- RHS become fresh binds inserted immediately BEFORE the current LetE
    -- so per-statement effect order is preserved. The outer EvalE at each
    -- CstDoBind/CstDoBare RHS is left in place; only its inner is walked.
    hoistDoChain :: ExprI -> MorlocMonad ExprI
    hoistDoChain (ExprI i (LetE binds rest)) = do
      (binds', hoists) <- collectBindHoists binds
      rest' <- hoistDoChain rest
      let letE = ExprI i (LetE binds' rest')
      foldrM (nestLet i) letE hoists
    hoistDoChain e@(ExprI parent _) = do
      (e', bs) <- hoistIn e
      foldrM (nestLet parent) e' bs

    -- Fold builds accBinds in reverse (final 'reverse') and accHoists in
    -- reverse-of-reverse so the concat at the end is O(N + total hoists)
    -- rather than O(N * total hoists) that a left-append accumulator would
    -- produce.
    collectBindHoists ::
      [(EVar, ExprI)] ->
      MorlocMonad ([(EVar, ExprI)], [(EVar, ExprI)])
    collectBindHoists bs0 = do
      (accBinds, revHoistLists) <- foldM step ([], []) bs0
      return (reverse accBinds, concat (reverse revHoistLists))
      where
        step (accBinds, revHoistLists) (v, rhs) = do
          (rhs', bs) <- hoistRhsOfDoBind rhs
          return ((v, rhs') : accBinds, bs : revHoistLists)

    -- Option A: the RHS of a '<-' bind (or bare non-final do-stmt) is a
    -- boundary. Bangs seal into a nested DoBlockE inside the RHS instead
    -- of propagating past the '<-' into the enclosing chain. Keeps
    -- 'a <- addI !x !y' legal and preserves the lexical firing point.
    hoistRhsOfDoBind :: ExprI -> MorlocMonad (ExprI, [(EVar, ExprI)])
    hoistRhsOfDoBind (ExprI i (EvalE inner)) = do
      inner' <- hoistBoundary inner
      return (ExprI i (EvalE inner'), [])
    hoistRhsOfDoBind e = hoistIn e

collectTags :: DAG MVar [AliasedSymbol] ExprI -> MorlocMonad ()
collectTags fullDag = do
  _ <- DAG.mapNodeM f fullDag
  return ()
  where
    -- \* add ManifoldConfigs associated with VarE types to MorlocMonad state
    -- \* the configs store the metadata associated with the term tags
    -- \* later we use the manifold indices to lookup things like runtime info
    --   (threads required and such)
    f :: ExprI -> MorlocMonad ()
    f (ExprI i (VarE config _)) = do
      -- Stamp the index into the config so that downstream label propagation
      -- (which copies the whole config to other midxes) preserves a pointer
      -- back to the original source position. Codegen reads this to render
      -- @{module}@/@{line}@/@{column}@ for the log template.
      let config' = case manifoldConfigLabel config of
            Just _ -> config {manifoldConfigLabelIdx = Just i}
            Nothing -> config
      MM.modify (\s -> s {stateManifoldConfig = Map.insert i config' (stateManifoldConfig s)})
    f (ExprI _ (ModE _ es)) = mapM_ f es
    f (ExprI _ (IstE _ _ es)) = mapM_ f es
    f (ExprI _ (AssE _ e es)) = mapM_ f (e : es)
    f (ExprI _ (LstE es)) = mapM_ f es
    f (ExprI _ (TupE es)) = mapM_ f es
    f (ExprI _ (NamE rs)) = mapM_ (f . snd) rs
    f (ExprI _ (AppE e es)) = mapM_ f (e : es)
    f (ExprI _ (LamE _ e)) = f e
    f (ExprI _ (AnnE e _)) = f e
    f (ExprI _ (LetE bindings body)) = mapM_ (f . snd) bindings >> f body
    f (ExprI _ (IfE c t e)) = f c >> f t >> f e
    f (ExprI _ (DoBlockE e)) = f e
    f (ExprI _ (EvalE e)) = f e
    f (ExprI _ (IntrinsicE _ es)) = mapM_ f es
    f _ = return ()

type GCMap = (Scope, Map Lang Scope)

{- | Add the following fields to state:
  * stateGeneralTypedefs           :: GMap Int MVar Scope
  * stateConcreteTypedefs          :: GMap Int MVar (Map Lang Scope)
-}
collectTypes :: DAG MVar [AliasedSymbol] ExprI -> MorlocMonad ()
collectTypes fullDag = do
  let typeDag = DAG.mapEdge (\xs -> [(x, y) | AliasedType x y <- xs]) fullDag
  result <- DAG.synthesizeNodes formTypes typeDag
  case result of
    Nothing -> MM.throwSystemError "Found cyclic module dependency"
    Just _ -> return ()
  where
    formTypes ::
      MVar ->
      ExprI ->
      [ ( MVar -- child module name
        , [(TVar, TVar)] -- alias map
        , GCMap
        )
      ] ->
      MorlocMonad GCMap
    formTypes m e0 childImports = do
      let (generalTypemap, concreteTypemapsIncomplete) = foldl inherit (AST.findTypedefs e0) childImports

      -- Here we are creating links from every indexed term in the module to the module
      -- sources and aliases. When the module abstractions are factored out later,
      -- this will be the only way to access module-specific info.
      let indices = AST.getIndices e0

      -- link concrete records to their full general forms
      let concreteTypemaps = Map.map (completeRecords generalTypemap) concreteTypemapsIncomplete

      s <- MM.get
      MM.put
        ( s
            { stateGeneralTypedefs = GMap.insertMany indices m generalTypemap (stateGeneralTypedefs s)
            , stateConcreteTypedefs = GMap.insertMany indices m concreteTypemaps (stateConcreteTypedefs s)
            }
        )

      return (generalTypemap, concreteTypemaps)

    inherit :: GCMap -> (key, [(TVar, TVar)], GCMap) -> GCMap
    inherit (thisGmap, thisCmap) (_, links, (gmap, cmap)) =
      let gmap' = filterAndSubstitute links gmap
          cmap' = Map.map (filterAndSubstitute links) cmap
       in ( Map.unionWith mergeEntries gmap' thisGmap
          , Map.unionWith (Map.unionWith mergeEntries) cmap' thisCmap
          )

{- | collect type definitions globally
  define:
    * stateUniversalGeneralTypedefs
    * stateUniversalConcreteTypedefs
-}
collectUniversalTypes :: DAG MVar a ExprI -> MorlocMonad ()
collectUniversalTypes dag = do
  universalGeneralScope <- getUniversalGeneralScope
  universalConcreteScope <- getUniversalConcreteScope universalGeneralScope

  -- Invariant 1: a transparent `type` alias may not carry a per-language
  -- override. A `type` alias chain must resolve to a single concrete type
  -- per language. Per-language overrides belong to newtypes or primitives.
  checkAliasLanguageFormConflict dag universalGeneralScope universalConcreteScope

  -- Invariant 2: a `newtype` wire-parent chain must not contain a cycle.
  checkNewtypeCycles universalGeneralScope

  -- Invariant 3: a typeclass instance may only be declared on the root of
  -- an alias tree. Transparent `type` aliases share all instances with
  -- their root, so an instance on an alias would be ambiguous with the
  -- root's instance.
  checkInstanceOnRoot dag universalGeneralScope

  s <- MM.get
  MM.put
    ( s
        { stateUniversalGeneralTypedefs = universalGeneralScope
        , stateUniversalConcreteTypedefs = universalConcreteScope
        }
    )
  where
    getUniversalGeneralScope :: MorlocMonad Scope
    getUniversalGeneralScope = do
      (GMap _ (Map.elems -> scopes)) <- MM.gets stateGeneralTypedefs
      return $ Map.unionsWith mergeEntries scopes

    getUniversalConcreteScope :: Scope -> MorlocMonad (Map Lang Scope)
    getUniversalConcreteScope gscope = do
      (GMap _ modMaps) <- MM.gets stateConcreteTypedefs
      let langs = unique $ concatMap Map.keys . Map.elems $ modMaps
      scopes <- mapM getLangScope langs
      return . Map.fromList $ zip langs scopes
      where
        getLangScope :: Lang -> MorlocMonad Scope
        getLangScope lang = do
          (GMap _ (Map.elems -> langMaps)) <- MM.gets stateConcreteTypedefs
          -- See note above, here we are completing any incomplete concrete
          -- record/table/object types
          let langMaps' = map (Map.map (completeRecords gscope)) langMaps
          return . Map.unionsWith mergeEntries . mapMaybe (Map.lookup lang) $ langMaps'

-- | Enforce Invariant 1: a transparent @type Foo = Bar@ alias may not
-- carry a per-language form. Primitives and newtypes are exempt -- both
-- are nominally distinct base forms and own their per-language overrides.
checkAliasLanguageFormConflict :: DAG MVar a ExprI -> Scope -> Map.Map Lang Scope -> MorlocMonad ()
checkAliasLanguageFormConflict dag gscope cscopes =
  let bad =
        [ (lang, v)
        | (lang, cscope) <- Map.toList cscopes
        , v <- Map.keys cscope
        , isTransparentAlias v gscope
        ]
  in case bad of
       [] -> return ()
       ((lang, v) : _) ->
         let (mIdx, mMod) = findTypedefSite lang v
             header = squotes (pretty (unTVar v)) <+> "is declared as a 'type' alias"
                 <+> "but has a per-language form for" <+> pretty lang <> "."
             advice = "Change 'type' to 'newtype' so" <+> squotes (pretty (unTVar v))
                 <+> "becomes a nominally distinct type that owns its native"
                 <+> "language forms."
         in case mIdx of
              -- A source caret already tells the user which file/line,
              -- so the module name would be redundant.
              Just i  -> MM.throwSourcedError i (header <> line <> advice)
              -- No source caret: at least name the declaring module so
              -- the user knows where to look.
              Nothing ->
                let whereClause = case mMod of
                      Just m  -> line <> "Declared in module" <+> squotes (pretty (unMVar m)) <> "."
                      Nothing -> mempty
                in MM.throwSystemError (header <> whereClause <> line <> advice)
  where
    isTransparentAlias :: TVar -> Scope -> Bool
    isTransparentAlias v scope = case Map.lookup v scope of
      Nothing -> False
      Just entries -> any isAliasKind entries
      where
        isAliasKind (_, _, _, _, TypedefAlias) = True
        isAliasKind _ = False

    -- Walk the per-module AST to find the per-language typedef
    -- declaration for (lang, v). Returns the declaration's AST index
    -- (so the error caret lands on the offending line) and the
    -- module name (for the error message). Both are best-effort: a
    -- transformation pass that drops indices would yield Nothing.
    findTypedefSite :: Lang -> TVar -> (Maybe Int, Maybe MVar)
    findTypedefSite lang v = case
        [ (i, m)
        | (m, (e, _)) <- Map.toList dag
        , i <- findLangTypedefIndices lang v e
        ] of
      ((i, m) : _) -> (Just i, Just m)
      []           -> (Nothing, Nothing)

    -- Recursively find indices of @type Lang => v ...@ declarations
    -- inside a module's expression tree.
    findLangTypedefIndices :: Lang -> TVar -> ExprI -> [Int]
    findLangTypedefIndices lang v = go
      where
        go (ExprI i (TypE (ExprTypeE (Just (lang', _)) v' _ _ _ _)))
          | lang == lang' && v == v' = [i]
          | otherwise = []
        go (ExprI _ (ModE _ es)) = concatMap go es
        go _ = []

-- | Enforce Invariant 2: the wire-parent edges between newtypes must form
-- a DAG. A cycle (e.g. @newtype A = B; newtype B = A@) is rejected at the
-- frontend. Primitives are leaves -- they have no wire-parent edges.
checkNewtypeCycles :: Scope -> MorlocMonad ()
checkNewtypeCycles gscope =
  let edges =
        [ (v, child)
        | (v, entries) <- Map.toList gscope
        , (_, body, _, _, TypedefNewtype) <- entries
        , child <- bodyHead body
        ]
      graph = Map.fromListWith (<>) [(v, [c]) | (v, c) <- edges]
  in case findCycle graph of
       Nothing -> return ()
       Just cyc ->
         MM.throwSourcedError 0 $
           "Cycle in newtype wire-parent chain:" <+>
           hsep (punctuate " ->" (map (pretty . unTVar) cyc))
  where
    bodyHead :: TypeU -> [TVar]
    bodyHead (VarU v) = [v]
    bodyHead (AppU (VarU v) _) = [v]
    bodyHead _ = []

    findCycle :: Map.Map TVar [TVar] -> Maybe [TVar]
    findCycle g = go Set.empty (Map.keys g)
      where
        go _ [] = Nothing
        go visited (v : vs)
          | Set.member v visited = go visited vs
          | otherwise = case dfs Set.empty [v] of
              Just cyc -> Just cyc
              Nothing -> go (Set.insert v visited) vs

        dfs _ [] = Nothing
        dfs path (v : _)
          | Set.member v path = Just [v]
          | otherwise =
              let children = Map.findWithDefault [] v g
                  path' = Set.insert v path
              in case mapMaybe (\c -> dfs path' [c]) children of
                   (cyc : _) -> Just (v : cyc)
                   [] -> Nothing

-- | Enforce Invariant 3: an @instance@ declaration's head type must not be
-- a transparent @type@ alias. Transparent aliases are fully interchangeable
-- with their root, so an instance on a leaf would collide with the root's
-- instance. @newtype@ and primitive heads (and bare type variables) are
-- accepted.
checkInstanceOnRoot :: DAG MVar a ExprI -> Scope -> MorlocMonad ()
checkInstanceOnRoot dag gscope =
  case findOffense of
    Nothing -> return ()
    Just (i, v) ->
      MM.throwSourcedError i $
        "Cannot declare instance on transparent alias"
          <+> squotes (pretty (unTVar v)) <> "."
          <> line
          <> "All members of an alias tree share a single instance."
          <+> "Either declare the instance for the root type, or change"
          <+> "the declaration of" <+> squotes (pretty (unTVar v))
          <+> "from 'type' to 'newtype' so it becomes a nominally distinct"
          <+> "type that owns its own instances."
  where
    findOffense :: Maybe (Int, TVar)
    findOffense = case
        [ (i, v)
        | (_, (e, _)) <- Map.toList dag
        , (i, ts) <- findInstanceTypes e
        , Just v <- map extractHead ts
        , isTransparentAlias v
        ] of
      ((i, v) : _) -> Just (i, v)
      []           -> Nothing

    isTransparentAlias :: TVar -> Bool
    isTransparentAlias v = case Map.lookup v gscope of
      Nothing -> False
      Just entries -> any isAliasKind entries
      where
        isAliasKind (_, _, _, _, TypedefAlias) = True
        isAliasKind _ = False

    -- Walk an expression tree to collect (instance-index, type-args) for
    -- every @IstE@ node. The index lets us locate the source caret on the
    -- offending @instance@ line.
    findInstanceTypes :: ExprI -> [(Int, [TypeU])]
    findInstanceTypes = go
      where
        go (ExprI i (IstE _ ts _)) = [(i, ts)]
        go (ExprI _ (ModE _ es)) = concatMap go es
        go _ = []

    -- Pull the outermost named-type head out of an instance type, skipping
    -- @forall@-bound variables. Returns @Nothing@ when the head is a bound
    -- variable, an existential, or some other shape that cannot resolve to
    -- a typedef-table entry.
    extractHead :: TypeU -> Maybe TVar
    extractHead = headOf Set.empty
      where
        headOf bnd (ForallU v t)     = headOf (Set.insert v bnd) t
        headOf bnd (VarU v)
          | Set.member v bnd         = Nothing
          | otherwise                = Just v
        headOf bnd (AppU (VarU v) _)
          | Set.member v bnd         = Nothing
          | otherwise                = Just v
        headOf bnd (AppU h _)        = headOf bnd h
        headOf _   _                 = Nothing

{- | links the general entries from records to their abbreviated concrete cousins.
For example:
  record (Person a) = Person {name :: Str, info a}
  record Py => Person a = "dict"
This syntax avoids the need to duplicate the entire entry
-}
completeRecords :: Scope -> Scope -> Scope
completeRecords gscope = Map.mapWithKey (completeRecord gscope)
  where
    completeRecord ::
      Scope ->
      TVar ->
      [([Either (TVar, Kind) TypeU], TypeU, ArgDoc, Bool, TypedefKind)] ->
      [([Either (TVar, Kind) TypeU], TypeU, ArgDoc, Bool, TypedefKind)]
    completeRecord g v xs = case Map.lookup v g of
      (Just ys) -> map (completeValue [(vs, t) | (vs, t, _, _, _) <- ys]) xs
      Nothing -> xs

    completeValue ::
      [([Either (TVar, Kind) TypeU], TypeU)] ->
      ([Either (TVar, Kind) TypeU], TypeU, ArgDoc, Bool, TypedefKind) ->
      ([Either (TVar, Kind) TypeU], TypeU, ArgDoc, Bool, TypedefKind)
    completeValue ((vs, NamU _ _ ps rs) : _) (_, NamU o v _ [], d, terminal, kind) = (vs, NamU o v ps rs, d, terminal, kind)
    completeValue _ x = x

-- merge type functions, names of generics do not matter
mergeEntries ::
  [([Either (TVar, Kind) TypeU], TypeU, ArgDoc, Bool, TypedefKind)] ->
  [([Either (TVar, Kind) TypeU], TypeU, ArgDoc, Bool, TypedefKind)] ->
  [([Either (TVar, Kind) TypeU], TypeU, ArgDoc, Bool, TypedefKind)]
mergeEntries xs0 ys0 = filter (isNovel ys0) xs0 <> ys0
  where
    isNovel ::
      [([Either (TVar, Kind) TypeU], TypeU, ArgDoc, Bool, TypedefKind)] -> ([Either (TVar, Kind) TypeU], TypeU, ArgDoc, Bool, TypedefKind) -> Bool
    isNovel [] _ = True
    isNovel ((vs2, t2, _, isTerminal1, kind1) : ys) x@(vs1, t1, _, isTerminal2, kind2)
      | (length vs1 == length vs2)
          && t1 == foldl (\t (v1, v2) -> rename v2 v1 t) t2 [(fst v1, fst v2) | (Left v1, Left v2) <- zip vs1 vs2]
          && isTerminal1 == isTerminal2
          && kind1 == kind2 =
          False
      | otherwise = isNovel ys x

-- clean imports
--   * only keep the exports of a module that are explicitly imported
--   * resolve any aliases
filterAndSubstitute :: [(TVar, TVar)] -> Scope -> Scope
filterAndSubstitute links typemap =
  let importedTypes = Map.filterWithKey (\k _ -> k `elem` map fst links) typemap
   in foldl typeSubstitute importedTypes links
  where
    typeSubstitute ::
      Scope -> -- imported map
      (TVar, TVar) -> -- source name and local alias
      Scope -- renamed map
    typeSubstitute typedefs (sourceName, localAlias) =
      case Map.lookup sourceName typedefs of
        (Just xs) ->
          Map.insert
            localAlias
            (map (\(a, b, c, d, e) -> (a, rename sourceName localAlias b, c, d, e)) xs)
            (Map.delete sourceName typedefs)
        Nothing -> typedefs

-- | Promote VarU to NatVarU, StrVarU, or RecVarU based on typedef param
-- kinds in all signatures. For each typedef like
-- `type Tensor2 (d1 :: Nat) (d2 :: Nat) a` or
-- `type Tagged (s :: Str) a` or `type Table n (r :: Rec)`, build a map
-- from type name to param kinds, then walk all types and upgrade KindType
-- to the corresponding kinded form for variables that appear in the
-- relevant positions. See plans/tables/04, 05, and 10.
refineKinds ::
  DAG MVar [AliasedSymbol] ExprI ->
  MorlocMonad (DAG MVar [AliasedSymbol] ExprI)
refineKinds dag = do
  let allParams = concat [collectAllTypeDefParams e | e <- DAG.nodes dag]
      hasKindedParam = any (\p -> either (\(_, k) -> case k of
                                                       KindNat -> True
                                                       KindStr -> True
                                                       KindRec -> True
                                                       KindList _ -> True
                                                       KindSet _ -> True
                                                       _ -> False) (const False) p)
      kindMap = Map.fromList [(v, map (either snd (const KindType)) ps) | (v, ps) <- allParams, hasKindedParam ps]
  if Map.null kindMap
    then return dag
    else DAG.mapNodeM (refineExprKinds kindMap) dag
  where
    collectAllTypeDefParams :: ExprI -> [(TVar, [Either (TVar, Kind) TypeU])]
    collectAllTypeDefParams (ExprI _ (ModE _ es)) = concatMap collectAllTypeDefParams es
    collectAllTypeDefParams (ExprI _ (TypE (ExprTypeE _ v ps _ _ _))) = [(v, ps)]
    collectAllTypeDefParams (ExprI _ (AssE _ e es)) = concatMap collectAllTypeDefParams (e:es)
    collectAllTypeDefParams (ExprI _ (IstE _ _ es)) = concatMap collectAllTypeDefParams es
    collectAllTypeDefParams _ = []

    refineExprKinds :: Map TVar [Kind] -> ExprI -> MorlocMonad ExprI
    refineExprKinds km expr = do
      e' <- AST.mapTypeInExprI (refineTypeKinds km) (promoteLabelKindsExpr expr)
      return (augmentImplicitConstraints e')

    -- | Pre-pass: for every SigE whose @enatLabels@ binds a parameter to a
    -- typed-position, infer the label's kind from the FunU argument type
    -- at that position and promote any matching VarU mentions throughout
    -- the type (and constraints) to the proper per-kind variable. This
    -- bridges the gap between the @f\@Str@ surface syntax and the
    -- promotion pass: by the time refineTypeKinds runs, the labels have
    -- already been stripped by Desugar's @extractLabels@, so the
    -- LabeledU wrappers are gone but the labels map still records which
    -- argument positions carried a kind hint.
    promoteLabelKindsExpr :: ExprI -> ExprI
    promoteLabelKindsExpr (ExprI i (ModE m es)) =
      ExprI i (ModE m (map promoteLabelKindsExpr es))
    promoteLabelKindsExpr (ExprI i (SigE (Signature v lng et))) =
      let kindedVars = labelDerivedKinds (enatLabels et) (etype et)
          newType = promoteKindedVarsR kindedVars (etype et)
          newCons = Set.map (AST.mapConstraint (promoteKindedVarsR kindedVars)) (econs et)
      in ExprI i (SigE (Signature v lng et { etype = newType, econs = newCons }))
    promoteLabelKindsExpr (ExprI i (AssE v e es)) =
      ExprI i (AssE v (promoteLabelKindsExpr e) (map promoteLabelKindsExpr es))
    promoteLabelKindsExpr (ExprI i (IstE c ts es)) =
      ExprI i (IstE c ts (map promoteLabelKindsExpr es))
    promoteLabelKindsExpr e = e

    -- | Given the @enatLabels@ map (var -> arg index) and the function
    -- type with labels stripped, look up the type at each labeled
    -- position and classify it as Str / Nat / List etc. Returns a
    -- kindedVars map ready for promoteKindedVarsR.
    labelDerivedKinds :: Map TVar Int -> TypeU -> Map TVar Kind
    labelDerivedKinds labels = go
      where
        go (ForallU _ t) = go t
        go (FunU args _) = Map.fromList
          [ (var, k)
          | (var, idx) <- Map.toList labels
          , idx < length args
          , Just k <- [kindOfLabelInner (args !! idx)]
          ]
        go _ = Map.empty

        kindOfLabelInner :: TypeU -> Maybe Kind
        kindOfLabelInner (VarU (TV "Str")) = Just KindStr
        kindOfLabelInner (VarU (TV "Int")) = Just KindNat
        kindOfLabelInner (AppU (VarU (TV "List")) [_]) = Just (KindList KindStr)
        kindOfLabelInner _ = Nothing

    -- After kind reclassification, walk every signature's TypeU and
    -- harvest implicit set-theoretic constraints from the structure of
    -- the type. The constraints are added to the signature's @econs@
    -- set so they fire through the existing @=>@ machinery.
    --
    -- Implicit constraints come from canonical operator forms:
    --
    -- - @RecExtendU k _ (RecVarU r)@ - extending a polymorphic Rec by
    --   a literal key emits @CDisjoint (SetLitU [StrLitU k]) (KeysU r)@.
    -- - @RecRestrictU r l@ - restricting a Rec to a list of keys emits
    --   @CSubset (ListToSetU l) (KeysU r)@.
    --
    -- Constraints are de-duplicated against the existing @econs@ set so
    -- explicit @=>@ clauses that match an implicit are not duplicated.
    augmentImplicitConstraints :: ExprI -> ExprI
    augmentImplicitConstraints (ExprI i (SigE (Signature v l (EType t cs doc labels)))) =
      let extras = collectImplicitConstraints t
          cs' = Set.union cs (Set.fromList extras)
      in ExprI i (SigE (Signature v l (EType t cs' doc labels)))
    augmentImplicitConstraints (ExprI i (ModE m es)) =
      ExprI i (ModE m (map augmentImplicitConstraints es))
    augmentImplicitConstraints (ExprI i (AssE v e es)) =
      ExprI i (AssE v (augmentImplicitConstraints e) (map augmentImplicitConstraints es))
    augmentImplicitConstraints (ExprI i (IstE c ts es)) =
      ExprI i (IstE c ts (map augmentImplicitConstraints es))
    augmentImplicitConstraints e = e

    -- Walk a TypeU and collect implicit constraints from the recognised
    -- operator forms. Recurses into every subterm.
    collectImplicitConstraints :: TypeU -> [Constraint]
    collectImplicitConstraints = go
      where
        go (RecExtendU k _ (RecVarU v)) =
          [CDisjoint (SetLitU [StrLitU k]) (KeysU (RecVarU v))]
        go (RecRestrictU r l) =
          [CSubset (ListToSetU l) (KeysU r)] ++ go r ++ go l
        go (FunU ts t) = concatMap go (t : ts)
        go (AppU h ts) = go h ++ concatMap go ts
        go (NamU _ _ ps rs) = concatMap go ps ++ concatMap (go . snd) rs
        go (ForallU _ t) = go t
        go (EffectU _ t) = go t
        go (OptionalU t) = go t
        go (RecExtendU _ a b) = go a ++ go b
        go (RecUnionU a b) = go a ++ go b
        go (RecDiffU a _) = go a
        go (RecIntersectU a b) = go a ++ go b
        go (RecDiffListU a b) = go a ++ go b
        go (LabeledU _ t) = go t
        go _ = []

    refineTypeKinds :: Map TVar [Kind] -> TypeU -> TypeU
    refineTypeKinds km t0 =
      let kindedVars = collectKindedVarsFromScope km t0
       in fillMissingKindArgs km (rewriteCrossKindBuiltins (promoteKindedVarsR kindedVars t0))

    -- | Pad missing kind-arg positions with 'VoidU' of the corresponding
    -- kind when a typedef'd constructor is under-applied. Args are
    -- placed by kind-aware partitioning (literals give exact kinds;
    -- bare vars default to KindType). Type-kind positions are never
    -- padded -- a missing type arg is an arity error, not gradual.
    --
    --   Vector T                   -->  Vector NatVoidU T
    --   Tensor3 h T                -->  Tensor3 h NatVoidU NatVoidU T
    --   Table 100                  -->  Table 100 RecVoidU
    --   Table { name :: Str }      -->  Table NatVoidU { name :: Str }
    --   Table                      -->  Table NatVoidU RecVoidU
    fillMissingKindArgs :: Map TVar [Kind] -> TypeU -> TypeU
    fillMissingKindArgs km = go
      where
        -- AppU-VarU-head does not recurse via 'go h': the bare-Var
        -- case below would fire on it and produce nested AppUs.
        go (AppU h@(VarU v) args) = case Map.lookup v km of
          Just paramKinds
            | length args < length paramKinds ->
                let args' = map go args
                    buckets = bucketByKind args'
                    typeParamCount = length [() | KindType <- paramKinds]
                    typeArgsProvided = length (Map.findWithDefault [] KindType buckets)
                in if typeArgsProvided < typeParamCount
                   then AppU h args'  -- arity error surfaces downstream
                   else AppU h (fillByBucket paramKinds buckets)
          _ -> AppU h (map go args)
        go (AppU h args) = AppU (go h) (map go args)
        -- Bare 'VarU v' for a typedef with only kind params expands to
        -- a full AppU with VoidU args (e.g. bare 'Table' becomes
        -- 'Table NatVoidU RecVoidU'). Typedefs with any Type param
        -- stay bare so the missing Type arg surfaces as arity error.
        go (VarU v)
          | Just paramKinds <- Map.lookup v km
          , not (null paramKinds)
          , all (/= KindType) paramKinds
          = AppU (VarU v) [VoidU k | k <- paramKinds]
          | otherwise = VarU v
        go (FunU ts t) = FunU (map go ts) (go t)
        go (ForallU v t) = ForallU v (go t)
        go (NamU r v ps rs) = NamU r v (map go ps) [(k, go t) | (k, t) <- rs]
        go (ExistU v (ts, tc) (rs, rc)) =
          ExistU v (map go ts, tc) ([(k, go t) | (k, t) <- rs], rc)
        go (EffectU effs t) = EffectU effs (go t)
        go (OptionalU t) = OptionalU (go t)
        go (OpU op args) = OpU op (map go args)
        go (LitU (LRec fs)) = LitU (LRec [(k, go v) | (k, v) <- fs])
        go (LitU (LList es)) = LitU (LList (map go es))
        go (LitU (LSet es)) = LitU (LSet (map go es))
        go (LabeledU n t) = LabeledU n (go t)
        go t = t

        -- Fill each parameter position by consuming from a bucket of
        -- args grouped by inferred kind. Positions with an empty bucket
        -- get VoidU of that kind.
        fillByBucket :: [Kind] -> Map Kind [TypeU] -> [TypeU]
        fillByBucket paramKinds buckets0 = go' paramKinds buckets0
          where
            go' []       _ = []
            go' (pk:pks) bs = case Map.lookup pk bs of
              Just (a:rest) -> a       : go' pks (Map.insert pk rest bs)
              _             -> VoidU pk : go' pks bs

        -- Group args by their inferred kind, preserving arg order.
        bucketByKind :: [TypeU] -> Map Kind [TypeU]
        bucketByKind = foldr (\a -> Map.insertWith (++) (inferArgKind a) [a]) Map.empty

        -- Classify an arg to a kind: literals and kind-specific carriers
        -- give exact kinds; operator applications inherit their result
        -- kind from the operator tag; bare vars default to KindType.
        inferArgKind :: TypeU -> Kind
        inferArgKind (LitU (LNat _))   = KindNat
        inferArgKind (LitU (LStr _))   = KindStr
        inferArgKind (LitU (LRec _))   = KindRec
        inferArgKind (LitU (LList _))  = KindList KindStr
        inferArgKind (LitU (LSet _))   = KindSet KindStr
        inferArgKind (KVarU (_, k))    = k
        inferArgKind (VoidU k)         = k
        inferArgKind (ListVarU _)      = KindList KindStr
        inferArgKind ListVoidU         = KindList KindStr
        inferArgKind (SetVarU _)       = KindSet KindStr
        inferArgKind SetVoidU          = KindSet KindStr
        inferArgKind (OpU op _)        = opResultKind op
        inferArgKind _                 = KindType

        -- Result kind of each type-level operator, used to classify
        -- OpU-headed args in @bucketByKind@. Without this the surface
        -- form @Table {x = Int}@ (which parses as OpU OpRecExtend ...)
        -- would fall through to KindType and get dropped during the
        -- gradual-arg fill.
        opResultKind :: OpTag -> Kind
        opResultKind OpNatAdd       = KindNat
        opResultKind OpNatSub       = KindNat
        opResultKind OpNatMul       = KindNat
        opResultKind OpNatDiv       = KindNat
        opResultKind OpStrConcat    = KindStr
        opResultKind OpRecExtend    = KindRec
        opResultKind OpRecUnion     = KindRec
        opResultKind OpRecIntersect = KindRec
        opResultKind OpRecRestrict  = KindRec
        opResultKind OpRecDiffList  = KindRec
        opResultKind OpRecSingleton = KindRec
        opResultKind OpListApp      = KindList KindStr
        opResultKind OpSetUnion     = KindSet KindStr
        opResultKind OpSetInter     = KindSet KindStr
        opResultKind OpSetDiff      = KindSet KindStr
        opResultKind OpKeys         = KindSet KindStr
        opResultKind OpListToSet    = KindSet KindStr
        opResultKind OpSize         = KindNat
        opResultKind OpProjectField = KindType

    -- | Recognise upper-case names of cross-kind builtins (Keys, Size,
    -- ListToSet) used as function-application heads in a type
    -- expression and rewrite them to the corresponding TypeU
    -- constructor. Recognition is by name (matching morloc's
    -- "uppercase identifiers are types" convention) so users write
    -- @Keys r@ where @r@ has Rec kind and the typechecker sees
    -- @KeysU r@. Unrecognised names are left as ordinary type
    -- applications so user-defined types named e.g. \"Foo\" still work.
    --
    -- Recurses into all subterms so nested uses get rewritten too.
    rewriteCrossKindBuiltins :: TypeU -> TypeU
    rewriteCrossKindBuiltins = goRW
      where
        goRW (AppU (VarU (TV "Keys")) [r]) = KeysU (asRec (goRW r))
        goRW (AppU (VarU (TV "ListToSet")) [l]) = ListToSetU (asList (goRW l))
        goRW (AppU (VarU (TV "Size")) [c]) = SizeU (goRW c)
          -- Size is kind-dispatched (List / Set / Rec). Don't force a
          -- kind here; let downstream kindMap classify.
        goRW (AppU (VarU (TV "ProjectField")) [r, f]) =
          ProjectFieldU (asRec (goRW r)) (asStr (goRW f))
        -- @Singleton k v@: a one-field Rec with a Str-kinded key (literal
        -- or variable) and a Type-kinded value. Reduces to RecExtendU
        -- when the key is solved at the call site.
        goRW (AppU (VarU (TV "Singleton")) [k, v]) =
          RecSingletonU (asStr (goRW k)) (goRW v)
        -- @Restrict r l@: restrict the Rec @r@ to fields whose names
        -- appear in the List @l@. Surface alternative to the `#`
        -- operator (which would clash with morloc's `#{...}` string
        -- interpolation if used as an inline binary operator).
        goRW (AppU (VarU (TV "Restrict")) [r, l]) =
          RecRestrictU (asRec (goRW r)) (asList (goRW l))
        goRW (AppU h ts) = AppU (goRW h) (map goRW ts)
        goRW (FunU ts t) = FunU (map goRW ts) (goRW t)
        goRW (ForallU v t) = ForallU v (goRW t)
        goRW (NamU r v ps rs) = NamU r v (map goRW ps) [(k, goRW t) | (k, t) <- rs]
        goRW (ExistU v (ts, tc) (rs, rc)) =
          ExistU v (map goRW ts, tc) ([(k, goRW t) | (k, t) <- rs], rc)
        goRW (EffectU effs t) = EffectU effs (goRW t)
        goRW (OptionalU t) = OptionalU (goRW t)
        -- Unified carriers: uniform recursion across all operators and literal payloads.
        goRW (OpU op args) = OpU op (map goRW args)
        goRW (LitU (LRec fs)) = LitU (LRec [(k, goRW v) | (k, v) <- fs])
        goRW (LitU (LList es)) = LitU (LList (map goRW es))
        goRW (LitU (LSet es)) = LitU (LSet (map goRW es))
        goRW (LabeledU n t) = LabeledU n (goRW t)
        goRW t = t

        -- Force a TypeU into the Rec / List / Str kind by promoting
        -- bare 'VarU's to their kinded counterparts. Used at the
        -- argument positions of cross-kind builtins so explicit
        -- constraint expressions like @Keys r@ commit @r@ to the Rec
        -- kind even when the surrounding context didn't already
        -- classify it.
        asRec (VarU v) = RecVarU v
        asRec t = t

        asList (VarU v) = ListVarU v
        asList t = t

        asStr (VarU v) = StrVarU v
        asStr t = t

    -- Collect variables that appear in kinded positions according to typedef
    -- param kinds. Returns a map from var name to its required kind. In
    -- conflicts (same var used in multiple kinded positions), the first
    -- encountered kind wins; this is consistent with how the typechecker
    -- discovers kinds.
    collectKindedVarsFromScope :: Map TVar [Kind] -> TypeU -> Map TVar Kind
    collectKindedVarsFromScope km = go
      where
        go (AppU (VarU v) args) = case Map.lookup v km of
          Just kinds -> Map.unions $
            [case (k, a) of
               (KindNat, _) -> goSlot KindNat a
               (KindStr, _) -> goSlot KindStr a
               (KindRec, _) -> goSlot KindRec a
               (KindList _, _) -> goSlot k a
               (KindSet _, _) -> goSlot k a
               _ -> go a
            | (k, a) <- zip kinds args]
            ++ [go a | a <- drop (length kinds) args]
          Nothing -> Map.unions (map go args)
        go (NatVarU v) = Map.singleton v KindNat
        go (StrVarU v) = Map.singleton v KindStr
        go (RecVarU v) = Map.singleton v KindRec
        go (ForallU _ inner) = go inner
        go (FunU args ret) = Map.unions (go ret : map go args)
        go (NamU _ _ ps rs) = Map.unions (map go ps ++ map (go . snd) rs)
        go (EffectU _ inner) = go inner
        go (OptionalU inner) = go inner
        go (NatAddU a b) = Map.union (goNat a) (goNat b)
        go (NatMulU a b) = Map.union (goNat a) (goNat b)
        go (NatSubU a b) = Map.union (goNat a) (goNat b)
        go (NatDivU a b) = Map.union (goNat a) (goNat b)
        go (StrConcatU a b) = Map.union (goStr a) (goStr b)
        go (RecExtendU _ a b) = Map.union (go a) (goRec b)
        go (RecUnionU a b) = Map.union (goRec a) (goRec b)
        go (RecDiffU a _) = goRec a
        go (RecIntersectU a b) = Map.union (goRec a) (goRec b)
        go (LabeledU _ inner) = go inner
        go _ = Map.empty

        -- A type sitting in a known-kind slot. Variables get promoted to
        -- that kind; operator results inherit kindedness from their
        -- operands. Mixed kinds (e.g. RecExtendU's field-type slot is Type-
        -- kinded; the key is Str-kinded) are dispatched recursively.
        goSlot :: Kind -> TypeU -> Map TVar Kind
        goSlot KindNat = goNat
        goSlot KindStr = goStr
        goSlot KindRec = goRec
        goSlot KindType = go
        -- List and Set kinds: variables in these slots are List/Set kinded.
        -- The Stage 8/9/10 solvers will need parallel goList / goSet
        -- traversals once the corresponding TypeU constructors land. For
        -- now any variable seen in the slot is classified by the slot.
        goSlot k@(KindList _) = goCollection k
        goSlot k@(KindSet _) = goCollection k

        -- Single-pass collection-kind classifier: any variable found
        -- gets the slot's kind. Recurses through structural operators
        -- (which were initially parsed as NatAddU / NatSubU because `+`
        -- and `-` are kind-overloaded at the surface) so that variables
        -- nested inside `l + ['z]` get classified as List rather than
        -- Nat. Falls back to `go` for unrecognised subterms.
        goCollection :: Kind -> TypeU -> Map TVar Kind
        goCollection slotK (VarU v@(TV name))
          | not (T.null name), isLower (T.head name) = Map.singleton v slotK
          | otherwise = Map.empty
        goCollection slotK (NatVarU v) = Map.singleton v slotK
        goCollection slotK (StrVarU v) = Map.singleton v slotK
        goCollection slotK (RecVarU v) = Map.singleton v slotK
        goCollection slotK (ListVarU v) = Map.singleton v slotK
        goCollection slotK (SetVarU v) = Map.singleton v slotK
        goCollection slotK (NatAddU a b) =
          Map.union (goCollection slotK a) (goCollection slotK b)
        goCollection slotK (NatSubU a b) =
          Map.union (goCollection slotK a) (goCollection slotK b)
        goCollection slotK (NatMulU a b) =
          Map.union (goCollection slotK a) (goCollection slotK b)
        goCollection slotK (ListAppU a b) =
          Map.union (goCollection slotK a) (goCollection slotK b)
        goCollection slotK (ListLitU es) =
          Map.unions (map (goCollection slotK) es)
        goCollection slotK (SetUnionU a b) =
          Map.union (goCollection slotK a) (goCollection slotK b)
        goCollection slotK (SetInterU a b) =
          Map.union (goCollection slotK a) (goCollection slotK b)
        goCollection slotK (SetDiffU a b) =
          Map.union (goCollection slotK a) (goCollection slotK b)
        goCollection _ t = go t

        -- In a Nat-kinded slot, any lowercase-named variable is Nat-kinded,
        -- regardless of an earlier (Desugar-pass) classification. This
        -- override is essential because Desugar's collectNatVars / etc are
        -- syntactic heuristics that don't see typedef-context info.
        goNat (VarU v@(TV name))
          | not (T.null name), isLower (T.head name) = Map.singleton v KindNat
          | otherwise = Map.empty
        goNat (NatVarU v) = Map.singleton v KindNat
        goNat (StrVarU v) = Map.singleton v KindNat
        goNat (RecVarU v) = Map.singleton v KindNat
        goNat (NatAddU a b) = Map.union (goNat a) (goNat b)
        goNat (NatMulU a b) = Map.union (goNat a) (goNat b)
        goNat (NatSubU a b) = Map.union (goNat a) (goNat b)
        goNat (NatDivU a b) = Map.union (goNat a) (goNat b)
        goNat t = go t

        goStr (VarU v@(TV name))
          | not (T.null name), isLower (T.head name) = Map.singleton v KindStr
          | otherwise = Map.empty
        goStr (NatVarU v) = Map.singleton v KindStr
        goStr (StrVarU v) = Map.singleton v KindStr
        goStr (RecVarU v) = Map.singleton v KindStr
        goStr (StrConcatU a b) = Map.union (goStr a) (goStr b)
        goStr t = go t

        goRec (VarU v@(TV name))
          | not (T.null name), isLower (T.head name) = Map.singleton v KindRec
          | otherwise = Map.empty
        goRec (NatVarU v) = Map.singleton v KindRec
        goRec (StrVarU v) = Map.singleton v KindRec
        goRec (RecVarU v) = Map.singleton v KindRec
        -- NatAddU between Rec-shaped operands is the polymorphic `+` for
        -- record union (gets reclassified to RecUnionU after promotion).
        -- NatSubU between a Rec-shaped operand and a Str literal is the
        -- polymorphic `-` for record difference (gets reclassified to
        -- RecDiffU after promotion). See plans/tables/10-rec-solver-decidability.md.
        goRec (NatAddU a b) = Map.union (goRec a) (goRec b)
        goRec (NatSubU a b) = Map.union (goRec a) (goStr b)
        goRec (RecExtendU _ a b) = Map.union (go a) (goRec b)
        goRec (RecUnionU a b) = Map.union (goRec a) (goRec b)
        goRec (RecDiffU a _) = goRec a
        goRec (RecIntersectU a b) = Map.union (goRec a) (goRec b)
        goRec t = go t

    -- True iff a TypeU is structurally a Rec-kinded value (variable, empty,
    -- extension, union, difference, intersection, or erased phantom). Used
    -- to reclassify polymorphic `+`/`-` operators after variable promotion.
    isRecLike :: TypeU -> Bool
    isRecLike (RecVarU _) = True
    isRecLike RecEmptyU = True
    isRecLike (RecExtendU _ _ _) = True
    isRecLike (RecUnionU _ _) = True
    isRecLike (RecDiffU _ _) = True
    isRecLike (RecIntersectU _ _) = True
    isRecLike (RecRestrictU _ _) = True
    isRecLike (RecDiffListU _ _) = True
    isRecLike (RecSingletonU _ _) = True
    isRecLike RecVoidU = True
    isRecLike _ = False

    isListLike :: TypeU -> Bool
    isListLike (ListVarU _) = True
    isListLike (ListLitU _) = True
    isListLike (ListAppU _ _) = True
    isListLike ListVoidU = True
    isListLike _ = False

    isSetLike :: TypeU -> Bool
    isSetLike (SetVarU _) = True
    isSetLike SetEmptyU = True
    isSetLike (SetUnionU _ _) = True
    isSetLike (SetInterU _ _) = True
    isSetLike (SetDiffU _ _) = True
    isSetLike SetVoidU = True
    isSetLike _ = False

    -- Promote VarU to NatVarU, StrVarU, or RecVarU per the kind map. Also
    -- override any incorrect Desugar-pass classification: Desugar's
    -- collectNatVars may have overeagerly promoted a variable to NatVarU
    -- because it appeared inside a NatAddU/NatSubU/etc, but the surrounding
    -- typedef context might say it should be Rec or Str. refineKinds is the
    -- authoritative pass; it overrides Desugar's heuristic. Strips ForallU
    -- wrappers for those variables (they're implicitly quantified).
    promoteKindedVarsR :: Map TVar Kind -> TypeU -> TypeU
    promoteKindedVarsR kindedVars = go
      where
        go (VarU v) = case Map.lookup v kindedVars of
          Just KindNat -> NatVarU v
          Just KindStr -> StrVarU v
          Just KindRec -> RecVarU v
          Just (KindList _) -> ListVarU v
          Just (KindSet _) -> SetVarU v
          _ -> VarU v
        go (NatVarU v) = case Map.lookup v kindedVars of
          Just KindStr -> StrVarU v
          Just KindRec -> RecVarU v
          Just (KindList _) -> ListVarU v
          Just (KindSet _) -> SetVarU v
          _ -> NatVarU v
        go (StrVarU v) = case Map.lookup v kindedVars of
          Just KindNat -> NatVarU v
          Just KindRec -> RecVarU v
          Just (KindList _) -> ListVarU v
          Just (KindSet _) -> SetVarU v
          _ -> StrVarU v
        go (RecVarU v) = case Map.lookup v kindedVars of
          Just KindNat -> NatVarU v
          Just KindStr -> StrVarU v
          Just (KindList _) -> ListVarU v
          Just (KindSet _) -> SetVarU v
          _ -> RecVarU v
        go (ListVarU v) = case Map.lookup v kindedVars of
          Just KindNat -> NatVarU v
          Just KindStr -> StrVarU v
          Just KindRec -> RecVarU v
          Just (KindSet _) -> SetVarU v
          _ -> ListVarU v
        go (SetVarU v) = case Map.lookup v kindedVars of
          Just KindNat -> NatVarU v
          Just KindStr -> StrVarU v
          Just KindRec -> RecVarU v
          Just (KindList _) -> ListVarU v
          _ -> SetVarU v
        go (ForallU v t)
          | Map.member v kindedVars = go t  -- strip ForallU for kinded vars
        go (ForallU v t) = ForallU v (go t)
        go (FunU ts t) = FunU (map go ts) (go t)
        go (AppU t ts) = AppU (go t) (map go ts)
        go (NamU o v ps rs) = NamU o v (map go ps) [(k, go t) | (k, t) <- rs]
        go (EffectU effs t) = EffectU effs (go t)
        go (OptionalU t) = OptionalU (go t)
        go (ExistU v (ps, pc) (rs, rc)) = ExistU v (map go ps, pc) (map (second go) rs, rc)
        go t@(NatLitU _) = t
        -- Reclassify Nat-kinded operators that operate on Rec-kinded
        -- operands (after variable promotion). This enables surface
        -- syntax like `r + {f=a}` (parsed as NatAddU) to mean the Rec
        -- union RecUnionU. See plans/tables/10-rec-solver-decidability.md.
        go (NatAddU a b) =
          let a' = go a; b' = go b
           in if      isListLike a' || isListLike b' then ListAppU a' b'
              else if isSetLike  a' || isSetLike  b' then SetUnionU a' b'
              else if isRecLike  a' || isRecLike  b' then RecUnionU a' b'
              else NatAddU a' b'
        go (NatMulU a b) = NatMulU (go a) (go b)
        go (NatSubU a b) =
          let a' = go a; b' = go b
           in case b' of
                StrLitU f | isRecLike a' -> RecDiffU a' [f]
                -- `r - f` where f is a Str variable (introduced by an
                -- f@Str signature label) drops the single key f from r.
                -- Wrapped as a singleton list so RecDiffListU's reducer
                -- handles the deferred-then-substituted lifecycle: when
                -- f gets solved at the call site, the list goes ground
                -- and reduceRecDiffList strips the key.
                StrVarU _ | isRecLike a' -> RecDiffListU a' (ListLitU [b'])
                -- `r - l` where l is a List drops every key in l from r.
                -- Routes to RecDiffListU; the new constructor's solver
                -- reduces ground forms via reduceRecDiffList.
                _ | isRecLike a' && isListLike b' -> RecDiffListU a' b'
                _ | isSetLike a' || isSetLike b' -> SetDiffU a' b'
                _ -> NatSubU a' b'
        go (NatDivU a b) = NatDivU (go a) (go b)
        go t@NatVoidU = t
        go t@(StrLitU _) = t
        go (StrConcatU a b) = StrConcatU (go a) (go b)
        go t@StrVoidU = t
        go t@RecEmptyU = t
        go (RecExtendU k a b) = RecExtendU k (go a) (go b)
        go (RecUnionU a b) = RecUnionU (go a) (go b)
        go (RecDiffU a ks) = RecDiffU (go a) ks
        go (RecIntersectU a b) = RecIntersectU (go a) (go b)
        go (RecRestrictU a b) = RecRestrictU (go a) (go b)
        go (RecDiffListU a b) = RecDiffListU (go a) (go b)
        go t@RecVoidU = t
        go (ListLitU es) = ListLitU (map go es)
        go (ListAppU a b) = ListAppU (go a) (go b)
        go t@ListVoidU = t
        go t@SetEmptyU = t
        go (SetLitU es) = SetLitU (map go es)
        go (SetUnionU a b) = SetUnionU (go a) (go b)
        go (SetInterU a b) = SetInterU (go a) (go b)
        go (SetDiffU a b) = SetDiffU (go a) (go b)
        go t@SetVoidU = t
        go (KeysU r) = KeysU (go r)
        go (ListToSetU l) = ListToSetU (go l)
        go (SizeU c) = SizeU (go c)
        go (ProjectFieldU r f) = ProjectFieldU (go r) (go f)
        go (RecSingletonU k v) = RecSingletonU (go k) (go v)
        go (LabeledU n t) = LabeledU n (go t)

collectSources :: DAG MVar [AliasedSymbol] ExprI -> MorlocMonad ()
collectSources fullDag = do
  _ <- DAG.mapNodeWithKeyM linkSources fullDag
  return ()
  where
    linkSources :: MVar -> ExprI -> MorlocMonad ExprI
    linkSources m e0 = do
      let objSources = AST.findSources e0
      let indices = AST.getIndices e0
      s <- MM.get
      MM.put (s {stateSources = GMap.insertManyWith (<>) indices m objSources (stateSources s)})
      return e0

-- Rename a variable. For example:
--   import maps (Map as HashMap, foo, bar)
--
-- Here all uses `Map` in anything imported from `maps` needs to
-- be renamed to `HashMap`. So we call:
--   rename (TV "Map") (TV "HashMap") x
-- where `x` is any term
rename :: TVar -> TVar -> TypeU -> TypeU
rename sourceName localAlias = f
  where
    f (VarU v)
      | v == sourceName = VarU localAlias
      | otherwise = VarU v
    f (ExistU v (ps, o1) (rs, o2)) =
      let v' = if v == sourceName then localAlias else v
       in ExistU v' (map f ps, o1) (map (second f) rs, o2)
    f (ForallU v t) = ForallU v (f t)
    f (FunU ts t) = FunU (map f ts) (f t)
    f (AppU t ts) = AppU (f t) (map f ts)
    f (NamU o v ts rs) =
      let v' = if v == sourceName then localAlias else v
       in NamU o v' (map f ts) (map (second f) rs)
    f (EffectU effs t) = EffectU effs (f t)
    f (OptionalU t) = OptionalU (f t)
    f t@(NatVarU _) = t
    f t@(NatLitU _) = t
    f (NatAddU a b) = NatAddU (f a) (f b)
    f (NatMulU a b) = NatMulU (f a) (f b)
    f (NatSubU a b) = NatSubU (f a) (f b)
    f (NatDivU a b) = NatDivU (f a) (f b)
    f t@NatVoidU = t
    f t@(StrVarU _) = t
    f t@(StrLitU _) = t
    f (StrConcatU a b) = StrConcatU (f a) (f b)
    f t@StrVoidU = t
    f t@(RecVarU _) = t
    f t@RecEmptyU = t
    f (RecExtendU k a b) = RecExtendU k (f a) (f b)
    f (RecUnionU a b) = RecUnionU (f a) (f b)
    f (RecDiffU a ks) = RecDiffU (f a) ks
    f (RecIntersectU a b) = RecIntersectU (f a) (f b)
    f (RecRestrictU a b) = RecRestrictU (f a) (f b)
    f (RecDiffListU a b) = RecDiffListU (f a) (f b)
    f t@RecVoidU = t
    f t@(ListVarU _) = t
    f (ListLitU es) = ListLitU (map f es)
    f (ListAppU a b) = ListAppU (f a) (f b)
    f t@ListVoidU = t
    f t@(SetVarU _) = t
    f t@SetEmptyU = t
    f (SetLitU es) = SetLitU (map f es)
    f (SetUnionU a b) = SetUnionU (f a) (f b)
    f (SetInterU a b) = SetInterU (f a) (f b)
    f (SetDiffU a b) = SetDiffU (f a) (f b)
    f t@SetVoidU = t
    f (KeysU r) = KeysU (f r)
    f (ListToSetU l) = ListToSetU (f l)
    f (SizeU c) = SizeU (f c)
    f (ProjectFieldU r fld) = ProjectFieldU (f r) (f fld)
    f (RecSingletonU k v) = RecSingletonU (f k) (f v)
    f (LabeledU n t) = LabeledU n (f t)
